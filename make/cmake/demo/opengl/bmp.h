#ifndef __BMP__H__
#define __BMP__H__

#ifndef _WIN32
#include <inttypes.h>

typedef int32_t LONG;
typedef uint16_t WORD;
typedef uint32_t DWORD;

typedef struct __attribute__ ((__packed__)) tagBITMAPFILEHEADER {
        WORD    bfType;
        DWORD   bfSize;
        WORD    bfReserved1;
        WORD    bfReserved2;
        DWORD   bfOffBits;
} BITMAPFILEHEADER;

typedef struct __attribute__ ((__packed__)) tagBITMAPINFOHEADER{
        DWORD      biSize;
        LONG       biWidth;
        LONG       biHeight;
        WORD       biPlanes;
        WORD       biBitCount;
        DWORD      biCompression;
        DWORD      biSizeImage;
        LONG       biXPelsPerMeter;
        LONG       biYPelsPerMeter;
        DWORD      biClrUsed;
        DWORD      biClrImportant;
} BITMAPINFOHEADER;
#endif

#endif
